//swapping two number by using third variable
#include<iostream>
using namespace std;
int main()
{
	 int a;
	 int b;
	 cout<<"enter the value of two numbers a and b"<<endl;
	 cin>>a>>b;
	 cout<<"befor swapping a = "<<a<<" "<<"b ="<<b<<endl;
	 int temp=a;
	 a=b;
	 b=temp;
	 cout<<"after swapping a = "<<a<<" "<<" b ="<<b<<endl;
	 
}

